# Медот get

contacts = {
    "Марат" : "+79284579121",
    "Аня" : "+79628863525",
    "Мама" : "+79183019320"
}

# Если мы пытаемся получить не существующий ключ, будет ошибка и программа завершится
print(contacts["Марат"])

# Если мы пытаемся получить не существующий ключ, на экран будет выведено None
print(contacts.get("Марко"))

# вторым значение мы можем указать значение которые будет выведена на экран если ключ не верный
print(contacts.get("Маратка", "Контакт не найден"))