# Кортеж, отличие от списков. В кортеже нельзя менять данные в отличие от списка

# Список пишется в квадратных скобках
names = ['Marat', 'Slavon', 'Edo']

# Меняем первое значение из списка на новое
names[0] = 'Марко'

print(names[0])


